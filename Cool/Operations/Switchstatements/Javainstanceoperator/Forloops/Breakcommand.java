package Cool.Operations.Switchstatements.Javainstanceoperator.Forloops;

public class Breakcommand {
public static void main(String[] args) {
        for (int i = 1; i <= 2; i++) {
            if (i == 3) {
                break;
            }
            System.out.println("Number: " + i);
        }
    }
}
    

